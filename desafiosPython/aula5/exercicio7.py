# Exercício 7
# Transformando graus Celcius em Fahrenheit
# Entrada
c = float(input("Insira a temperatura em Celsius: "))

# Processamento
# Forúla de conversão: (9*c/5)+32
f = 9*c/5 + 32
print("")

# Saída
print(f"A temperatura de {c}°C, se torna {f}°F")
print("---------------------------------------")